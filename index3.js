let current=0;
let userAnswers=new Array(questions.length).fill(null);

// create or refresh the question index (list of numbers)
function generateIndex(){
    const idxDiv = document.getElementById("question-index");
    if(!idxDiv) return;
    // always show the index; clear previous
    idxDiv.innerHTML = "";
    questions.forEach((q,i)=>{
        const btn = document.createElement("button");
        btn.innerText = i+1;
        btn.onclick = ()=>{
            current = i;
            loadQuestion();
        };
        if(current===i){
            btn.classList.add("active");
        }
        if(userAnswers[i]!==null){
            // mark answered questions with a different style
            btn.classList.add("answered");
        }
        idxDiv.appendChild(btn);
    });
}

function loadQuestion(){
if(current>=questions.length) return;
    // update index each time (so selected number is highlighted)
    generateIndex();
document.getElementById("question").innerText=
`Q${current+1}. ${questions[current].question}`;

let optionsDiv=document.getElementById("options");
optionsDiv.innerHTML="";

questions[current].options.forEach((opt,i)=>{
let div=document.createElement("div");
div.className="option";
div.innerText=opt;

if(userAnswers[current]===i){
div.classList.add("selected");
}

div.onclick=()=>{
userAnswers[current]=i;
loadQuestion();
};

optionsDiv.appendChild(div);
});
}

function nextQuestion(){
    if(current<questions.length-1){
        current++;
        loadQuestion();
    }
}

function prevQuestion(){
    if(current>0){
        current--;
        loadQuestion();
    }
}

function submitQuiz(){

let score=0;
let wrongCount=0;
let rightCount=0;
let wrongList="";

questions.forEach((q,i)=>{
    if(userAnswers[i]!==null){
        if(userAnswers[i]===q.answer){
            score+=1;
            rightCount++;
        }else{
            score-=0.25;
            wrongCount++;
            wrongList+=`\n<div class="review">\n<b>Q${i+1}:</b> ${q.question}<br>\n<span class="wrong">Your Answer:</span> ${q.options[userAnswers[i]]}<br>\n<span class="correct">Correct Answer:</span> ${q.options[q.answer]}\n</div>\n`;
        }
    }
});

document.getElementById("quizBox").innerHTML=`
<div class="result">
<h2>Quiz Completed 🎉</h2>
<h3>Total Score: ${score.toFixed(2)}</h3>
<h4>Attempted: ${userAnswers.filter(a=>a!==null).length}/${questions.length}</h4>
<h4>Correct: ${rightCount} &nbsp; Incorrect: ${wrongCount}</h4>
${wrongList ? "<h3>Wrong Answer Review</h3>"+wrongList : "<h3>Perfect Score 🔥</h3>"}
</div>
    <div class="notice">
    <p>📢 महत्वपूर्ण सूचना</p>
    <p>इस टेस्ट सीरीज़ के FREE PDF Solution के लिए हमारे Telegram Channel को जॉइन करें: 👉 <strong>@upexammantra</strong></p>
    </div>
`;

    // hide the question navigation bar after submission
    const nav = document.getElementById("question-index");
    if(nav){
        nav.style.display = "none";
    }
}

loadQuestion();